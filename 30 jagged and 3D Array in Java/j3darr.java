public class j3darr
{
    public static void main(String args[])
    {
        System.out.println("Program started in main()");


        // Can internal arrays have less number of values -> YES, it is possible
        // This is called jagged array -> Where we are not sure about the number of columns

        int nums[][] = new int[3][];    // jagged

        // for every column, we are specifying the size of array

        nums[0] = new int[3];
        nums[1] = new int[4];
        nums[2] = new int[2];



    }
}
